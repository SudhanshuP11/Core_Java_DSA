package Java_Basics.Patterns;

public class SquarePatternPrint {
    public static void main(String[] args){
        System.out.println("Square Pattern: ");
        for(int i =1;i<=4;i++){
            for(int j=1;j<=4;j++){
                System.out.print("*"+" ");
            }
            System.out.println();
        }
    }
}
